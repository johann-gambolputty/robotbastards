
namespace Rb.Rendering.Contracts.Objects
{
	/// <summary>
	/// Polygon rendering modes
	/// </summary>
	public enum PolygonRenderMode
	{
		/// <summary>
		/// Polygons vertices are rendered as points
		/// </summary>
		Points,

		/// <summary>
		/// Polygon edges are rendered as edges
		/// </summary>
		Lines,

		/// <summary>
		/// Polygons are rendered filled
		/// </summary>
		Fill
	}
}
